    private void btnDisplayActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDisplayActionPerformed
        int from, to;
        from = Integer.parseInt(txtfield1.getText()); //input1
        to = Integer.parseInt(txtfield2.getText()); //input2
        
        String prime = "";
        for(int i = from; i <= to; i++){
            int counter = 0;
            for(int num = i; num >=1; num--){
                if(i % num == 0){
                    counter = counter + 1;
                }
            }
            if(counter == 2){
                prime = prime + i + " ";
            }           
        }txtaOutput.append(prime); //txtarea     
    }//GEN-LAST:event_btnDisplayActionPerformed

