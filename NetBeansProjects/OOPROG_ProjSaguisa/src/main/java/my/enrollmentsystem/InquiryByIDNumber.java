    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        //search button
        String id = jTextField1.getText();
        int n = StudentRecords.SearchID(id);
        
        if(n == -1){
            jLabel8.setText("ID number does not exist!"); //prompt
        }
        else if(StudentRecords.checkNumber(id)){
            jLabel8.setText("Invalid ID Number!"); //prompt
        }
        else if(!(id.length()==8)){
            jLabel8.setText("ID number must be 8-digits!"); //prompt
        }
        else {
            IdNum1 = StudentRecords.setCount();
            jLabel9.setText(StudentRecords.setlastname(n));
            jLabel10.setText(StudentRecords.setfirstname(n));
            jLabel11.setText(StudentRecords.setMiddle(n));
            jLabel12.setText(StudentRecords.setCourse(n));
            jLabel13.setText(StudentRecords.setYear(n));
            jLabel14.setText(StudentRecords.setStatus(n));
        }
        
    }//GEN-LAST:event_jButton1ActionPerformed

