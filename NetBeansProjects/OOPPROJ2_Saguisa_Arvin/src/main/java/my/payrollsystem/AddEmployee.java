    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        boolean valid = true; //validation
        String empIDNum = jTextField1.getText();
        String empLast =jTextField2.getText();
        String empFirst = jTextField3.getText();
        double hours = Double.parseDouble(jTextField6.getText());
        double rate = Double.parseDouble(jTextField7.getText());
        double other = Double.parseDouble(jTextField10.getText());
        
        //gender 
        char Gender = jTextField5.getText().charAt(0);
        String gender = String.valueOf(Gender);
             
         // department 
        String comboValue = jComboBox1.getSelectedItem().toString();
        jTextField4.setText(comboValue);
        jComboBox1.getSelectedItem();
        
        //employee number
        if(EmployeeRecs.checkNumber(empIDNum)){
            jLabel8.setText("Invalid Employee Number!"); //prompt
            valid = false;
        }
        else if(!(empIDNum.length()==8)){
            jLabel8.setText("ID Number must be 8-digits long!"); //prompt
            valid = false;
        }
        else if(size > 0){
            if (EmployeeRecs.checkempIDNumber(empIDNum, size)) {
                jLabel8.setText("ID Number is already taken!");
                valid = false;
            }
        }
        
        //LastName
        if(empLast.length()==0){
            jLabel8.setText("Please input a Last Name!"); //prompt
            valid = false;
        }
        else if(EmployeeRecs.checkLetters(empLast)){
            jLabel8.setText("Invalid Last Name!"); //prompt
            valid = false;                  
        } 
        //First Name
        else if(empFirst.length()==0){
            jLabel8.setText("First Name required!"); //prompt
            valid = false; 
        }
        else if(EmployeeRecs.checkLetters(empFirst)){
            jLabel8.setText("Invalid First Name!"); //prompt
            valid = false;                  
        }
        //gender
        else if (gender.length() == 0) {
            jLabel8.setText("Gender Field cannot be empty");
            valid = false;
        }
        else if(!(Gender == ('M') || Gender == ('F'))){
            jLabel8.setText("Gender must be only M or F");
            valid = false;
        }
        //Hours worked
        else if (jTextField6.getText().length() == 0) {
            jLabel8.setText("Hours Worked required!");
            valid = false;
        } 
        else if(!EmployeeRecs.checkLetters(jTextField6.getText())) {
            jLabel8.setText("Invalid Hours Worked input!");
            valid = false;
        }
        //Rate Per Hour
        else if (jTextField7.getText().length() == 0) {
            jLabel8.setText("Rate Per Hour required!");
            valid = false;
        } 
        else if(!EmployeeRecs.checkLetters(jTextField7.getText())) {
            jLabel8.setText("Invalid Rate per Hour!");
            valid = false;
        }
        //Other Deductions
        else if (jTextField6.getText().length() == 0) {
            jLabel8.setText("Other Deductions required!");
            valid = false;
        } 
        else if(!EmployeeRecs.checkLetters(jTextField10.getText())) {
            jLabel8.setText("Invalid Other Deductions!");
        }
        
        //proceed to add new employee
        if(valid == true){
        
        
        double SSS, tax, Net;
        double basic = hours * rate;
        
        //computation
        if((basic - (0.05 * basic)) > 500){
            SSS = 0.05 * basic;
        }
        else{
            SSS = 500;
        }
        
        if(basic<= 10000){
            tax = basic * 0.05;
        }
        else if(basic <= 30000){ tax = (500 + (.10 * (basic - 10000))); }
        else if(basic <= 70000){ tax = (2500 + (.15 * (basic - 30000))); }
        else if(basic <= 140000) { tax = (8500 + .20 * (basic - 70000)); }
        else if(basic <= 250000) {tax = (22500+(.25 * (basic - 140000))); }
        else if(basic <= 500000) { tax = (50000 + (.30 * (basic - 250000))); }
        else { tax = (125000 + (.32 * (basic - 500000))); }
        
        Net = basic - (other + SSS + tax);
                
        EmployeeRecs.getID(empIDNum,size);
        EmployeeRecs.getlastname(empLast, size);
        EmployeeRecs.getfirstname(empFirst, size);
        EmployeeRecs.getGender(Gender, size);
        EmployeeRecs.getDepartment(comboValue, size);
        EmployeeRecs.getHoursWorked(hours, size);
        EmployeeRecs.getRate(rate, size);
        EmployeeRecs.getOther(other, size);
        jLabel8.setText("Records successfully added!");
        EmployeeRecs.getSize1(size, 1);
        size++;
        EmployeeRecs.getSize(size);
        jLabel8.setText("Record Successfully added!");
        
        }
                
    }//GEN-LAST:event_jButton2ActionPerformed

